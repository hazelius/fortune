package main

import "testing"

func Test_run(t *testing.T) {
	type args struct {
		n  int
		k  int
		as []int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{name: "1", args: args{n: 3, k: 4, as: []int{1, 2, 3}}, want: 5},
		{name: "2", args: args{n: 1, k: 10, as: []int{9}}, want: 0},
		{name: "3", args: args{n: 2, k: 0, as: []int{0, 0}}, want: 1},
		{name: "4", args: args{n: 4, k: 100000, as: []int{100000, 100000, 100000, 100000}}, want: 665683269},
		{name: "5", args: args{n: 99, k: 99167, as: []int{23627, 42132, 9335, 45341, 70201, 96002, 80956, 2769, 70525, 23687, 947, 89940, 83630, 92382, 42371, 869, 81639, 72391, 89594, 91397, 7047, 50247, 81738, 48060, 29618, 40261, 18503, 47859, 59469, 94479, 234, 55321, 66789, 15763, 76977, 61183, 57545, 84966, 89244, 49147, 1292, 17664, 94770, 51081, 7620, 41982, 61178, 97029, 48590, 14911, 86466, 16274, 23271, 73032, 7356, 79638, 33460, 49056, 52156, 24572, 8187, 84404, 55579, 57008, 45320, 15316, 93175, 74439, 97865, 51348, 58865, 15956, 71602, 75660, 29645, 22452, 71290, 39129, 99029, 40227, 66844, 86490, 58039, 23333, 95416, 86611, 5634, 47166, 27247, 38733, 14861, 23930, 55850, 43924, 95281, 70886, 52435, 78168, 51882}}, want: 384688648},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := run(tt.args.n, tt.args.k, tt.args.as); got != tt.want {
				t.Errorf("run() = %v, want %v", got, tt.want)
			}
		})
	}
}
